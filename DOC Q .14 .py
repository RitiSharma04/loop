num=int(input("enter the num:-"))
b=str(num)
i=0
while i<len(b):
    if b[i]=="1":
        print("One")
    elif b[i]=="2":
        print("Two") 
    elif b[i]=="3":
        print("Three")
    elif b[i]=="4":
        print("Four")
    elif b[i]=="5":
        print("Five")
    elif b[i]=="6":
        print("Six")
    elif b[i]=="7":
        print("Savin")
    elif b[i]=="8":
        print("Eight")
    elif b[i]=="9":
        print("Nine")
    else:
        print("Zero")
    i=i+1


